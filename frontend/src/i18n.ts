function setDocDirection(lng: string) {
  if (typeof document !== 'undefined') {
    document.documentElement.dir = (lng === 'ar' || lng === 'ur') ? 'rtl' : 'ltr'
    document.documentElement.lang = lng === 'ur' ? 'ur' : lng === 'ar' ? 'ar' : 'en'
  }
}
setDocDirection(i18n.language)
i18n.on('languageChanged', setDocDirection)

